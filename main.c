int fputc(int _c, register FILE *_fp)
{
    EUSCI_A_UART_transmitData(EUSCI_A0_BASE, _c);

    return((unsigned char)_c);
}

int fputs(const char *_ptr, register FILE *_fp)
{
  unsigned int i, len;

  len = strlen(_ptr);

  for(i=0 ; i<len ; i++)
  {
      fputc(_ptr[i], _fp);
  }

  return len;
}
